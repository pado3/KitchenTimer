# run length encoding
# based upon https://nashidos.hatenablog.com/entry/2020/02/07/202427
# ex.   "0xFC,0x07,0xF8,0x0F,0x00,0x00" to "2 9 8 9 20"
#       0011 1111 1110 0000 0001 1111 1111 0000 0000 0000 0000 0000
#       00 11111-1111 00000-000 11111-1111 00000-00000-00000-00000
def rle(page):
    # make bit sequence of page data
    tmp = ""
    for p in page:
        tmp += format(p, '08b')[::-1]   # reverse bit order
    # print("debug: ", tmp)
    # encode
    ans = ''
    count = 1
    pre = tmp[0]
    for c in tmp[1:]:
        if c == pre:
            count += 1
        else:
            ans += str(count) + ','
            count = 1
            pre = c
    ans += str(count) + ','
    return ans

